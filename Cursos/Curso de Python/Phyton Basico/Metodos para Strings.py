mensagem = '     eu adoro comida caseira '

print(mensagem.lower()) # Letra minucula

print(mensagem.upper()) # Letra maiuscula

print(mensagem.capitalize()) # Primeira letra maiuscula

print(mensagem.find('c')) #index  01245....
print(mensagem.find('adoro'))

print(mensagem.replace('a', 'e')) # Substitui a primeira pela segunda
print(mensagem.replace('caseira', 'Feita em casa'))

print(mensagem.strip()) # Remove todos espços antes do 1º caractere
